    ArrayList<String> invoiceid, itemId, itemName;

    private void startup() {
        invoiceid = new ArrayList<String>();
        itemId = new ArrayList<String>();
        itemName = new ArrayList<String>();
        try {
            Statement s = main.DB.con().createStatement();
            Statement s1 = main.DB.con().createStatement();
            Statement s2 = main.DB.con().createStatement();
            Statement s3 = main.DB.con().createStatement();


            ResultSet rs = s.executeQuery("SELECT `auto_id`,'' FROM `sanasa`.`invoice`");
            while (rs.next()) {
                invoiceid.add(rs.getString(1));
            }

            ResultSet rs2 = s1.executeQuery("SELECT `id`,`name`,`subname`,`type` FROM `sanasa`.`inventory` WHERE id LIKE 'm%'");
            while (rs2.next()) {
                itemId.add(rs2.getString(1));
                itemName.add(rs2.getString(2) + "-" + rs2.getString(3) + "-" + rs2.getString(4));
            }

            ResultSet rs3 = s2.executeQuery("SELECT `id`,`name`,`subname`,`type` FROM `sanasa`.`inventory` WHERE id LIKE 'p%'");
            while (rs3.next()) {
                itemId.add(rs3.getString(1));
                itemName.add(rs3.getString(2) + "-" + rs3.getString(3) + "-" + rs3.getString(4));
            }
            main.AutoComplete.setupAutoComplete(txtInvoiceID, invoiceid);
            main.AutoComplete.setupAutoComplete(txtitemid, itemId);
            main.AutoComplete.setupAutoComplete(txtitemname, itemName);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Complete Loading Error");
            e.printStackTrace();
        }
    }

