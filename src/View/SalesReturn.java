    private void txtitemidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtitemidKeyReleased
        try {
            if (txtitemid.getText().isEmpty()) {
                txtitemname.setText(null);
                chdamage.setSelected(false);
                txtqty.setText(null);
            } else {
                Statement st = main.DB.con().createStatement();
                ResultSet rs = st.executeQuery("SELECT `name`,`subname`,`type` FROM "
                        + "`sanasa`.`inventory` WHERE `id`='" + txtitemid.getText() + "'");

                if (rs.next()) {
                    txtitemname.setText(rs.getString(1) + "-" + rs.getString(2) + "-" + rs.getString(3));
                }

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_txtitemidKeyReleased

