    private void txtInvoiceIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInvoiceIDKeyReleased
        try {
            Statement st = main.DB.con().createStatement();
            ResultSet rs = st.executeQuery("SELECT c.`name`,i.`date` FROM `invoice` i,`contact` c WHERE i.`Contact_nic`=c.`nic`"
                    + " AND i.`auto_id`='" + txtInvoiceID.getText() + "'");

            if (rs.next()) {
                txtContactName.setText(rs.getString(1));
                txtinvoicedate.setText(rs.getString(2));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_txtInvoiceIDKeyReleased

